Базові поняття та ввід/вивід
Мова Python підтримує динамічну та сильну типізацію.
Введення даних здійснюється функцією input(). Введені дані завжди є рядком (str), тому для чисел потрібне перетворення типів int() або float().
Виведення даних здійснюється функцією print(). Вона має додаткові параметри: sep (роздільник між аргументами) та end (символ в кінці, за замовчуванням новий рядок).
Python:
x = int(input('Введіть число:'))
print(1, 6, 7, 8, 9, sep=':')

Типи даних:
Числа: цілі (int), дійсні (float), комплексні (complex), логічні (bool).
Незмінні послідовності: рядки (str), кортежі (tuple).
Змінювані колекції: списки (list), словники (dict).

Арифметичні та логічні операції:
Арифметика: + (додавання), - (віднімання), * (множення), / (ділення з плаваючою точкою), // (цілочисельне ділення), % (залишок), ** (піднесення до степеню).
Логічні оператори: == (рівність), != (нерівність), <, <=, >, >=, in (включення), not (заперечення), and, or.

Робота з рядками (str):
Рядки можна створювати в одинарних або подвійних лапках. Для багаторядкових речень використовуються потрійні лапки """...""". Керуючі символи: \n (новий рядок), \t (табуляція), \\ (похила риса).
Звернення та зрізи: Звернення до символу відбувається через індекси string[0] або від'ємні індекси string[-1]. Зрізи виконуються за формулою [start:end:step].
Методи та форматування:
