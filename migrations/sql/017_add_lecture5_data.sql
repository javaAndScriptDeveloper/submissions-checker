Робота з текстовими файлами
Для роботи з файлами у Python використовується вбудована функція open(). Вона повертає файловий об'єкт, з яким можна виконувати операції читання або запису. Після завершення роботи файл обов'язково треба закривати методом close(), щоб звільнити ресурси системи.

Режими відкриття файлів:
'r' (read) — читання (за замовчуванням). Якщо файлу немає, виникне помилка FileNotFoundError.
'w' (write) — запис. Якщо файл існує, його вміст буде повністю видалено. Якщо ні — створено новий.
'a' (append) — дозапис. Дані додаються в кінець існуючого файлу, не видаляючи попередні.
'b' (binary) — бінарний режим (наприклад, 'rb' для читання зображень).
'r+' / 'w+' — читання та запис одночасно.

Контекстні менеджери (with):
Сучасний і найбезпечніший стандарт роботи з файлами — використання конструкції with. Вона гарантує, що файл буде автоматично закрито після виконання блоку коду, навіть якщо всередині виникне помилка. Завжди варто вказувати кодування encoding='utf-8', щоб уникнути проблем з відображенням тексту.

Методи читання та запису:
file.read() — зчитує весь вміст файлу як один великий рядок.
file.read(n) — зчитує рівно n символів.
file.readline() — зчитує лише один рядок до символу переведення рядка \n.
file.readlines() — повертає список, де кожен елемент — це рядок з файлу.
file.write('текст') — записує рядок у файл.

with open('data.txt', 'w', encoding='utf-8') as file:
    file.write('Перший рядок\n')
    file.write('Другий рядок\n')

with open('data.txt', 'r', encoding='utf-8') as file:
    content = file.readlines()
    for line in content:
