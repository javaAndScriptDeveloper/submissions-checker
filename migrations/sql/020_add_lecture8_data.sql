Об'єктно-орієнтоване програмування (ООП)
ООП — це парадигма програмування, де програма будується навколо «об'єктів», які об'єднують у собі дані (атрибути) та поведінку (методи).

Класи та Об'єкти:
Клас (class) — це креслення або шаблон. Об'єкт — це конкретний екземпляр класу.
- Метод __init__(self, ...) — це конструктор класу. Він автоматично викликається при створенні об'єкта і слугує для ініціалізації початкових атрибутів.
- Параметр self — це посилання на конкретний об'єкт. Він є обов'язковим першим параметром для всіх методів екземпляра.

Три принципи (стовпи) ООП:
1. Інкапсуляція — приховування внутрішніх даних та реалізації. У Python це робиться умовно: один символ підкреслення _attr означає «захищений» (protected) атрибут, а два __attr — «приватний» (private), до якого не можна звернутися безпосередньо ззовні.
2. Спадкування — створення нових класів (дочірніх) на базі існуючих (батьківських). Дочірній клас переймає всі методи і атрибути, а також може додавати свої. Для виклику методу батьківського класу використовується функція super().
3. Поліморфізм — можливість використовувати однаковий інтерфейс для об'єктів різних типів. Наприклад, різні класи можуть мати метод з однаковою назвою .draw(), але кожен реалізує його по-своєму.

Магічні методи (Dunder methods):
- __str__(self) — визначає, що поверне функція print(obj) (людиночитабельний опис).
- __repr__(self) — технічний опис об'єкта для розробників.

class Person:
    def __init__(self, name, age):
        self.name = name
        self.__age = age  # Приватний атрибут

    def get_age(self):
        return self.__age

class Student(Person):  # Спадкування
    def __init__(self, name, age, university):
        super().__init__(name, age)  # Виклик конструктора Person
        self.university = university

    def __str__(self):
        return f"Студент {self.name} навчається у {self.university}"

s = Student("Олег", 20, "ДНУ")
print(s)  # Викличе метод __str__
